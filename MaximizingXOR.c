int maxXor(int l, int r) {
    int xor = l ^ r;
    int a = 0;
    while(pow(2,a)< xor)a++;
    return pow(2,a) - 1 ;
}

int main() {
    int res;
    int _l;
    scanf("%d", &_l);
    
    int _r;
    scanf("%d", &_r);
    
    res = maxXor(_l, _r);
    printf("%d", res);
    
    return 0;
}
